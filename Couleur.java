package cartes;

/**
 * Représente les différentes couleurs possibles d'une carte
 * dans un jeu de cartes standard.
 */
public enum Couleur {

    /** Couleur Cœur (♥) */
    COEUR,

    /** Couleur Carreau (♦) */
    CARREAU,

    /** Couleur Trèfle (♣) */
    TREFLE,

    /** Couleur Pique (♠) */
    PIQUE
}
